package cz.incad.relief3.rdcz.uniimporter.utils;

/** ****************************************************************************
 *
 * @author martin.novacek@incad.cz
 */
public class OnePrispevatelIdentificator {
    public final String value;
    public final String sigla;
    public final String zarazeniUser;
    public String katalog = null;
    public String digKnihovna = null;
    public String email = null;


    /** ************************************************************************
     * 
     * @param value
     * @param sigla
     * @param zarazeniUser 
     */
    public OnePrispevatelIdentificator(String value, String sigla, String zarazeniUser, String email) {
        this.value = value;
        this.sigla = sigla;
        this.zarazeniUser = zarazeniUser;
        if (email != null && !email.isEmpty()) {
            this.email = email;
        }
    }

}
